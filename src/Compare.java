/*书上P50例3.17*/
public class Compare {
	public static void main(String[] args) {
        int number1 = 4;
        int number2 = 5;
        /*依次将变量number1与number2的比较结果输出*/
        System.out.println("number1>number2的返回值为:" + (number1>number2));
        System.out.println("number1<number2的返回值为:" + (number1<number2));
        System.out.println("number1==number2的返回值为:" + (number1==number2));
        System.out.println("number1>=number2的返回值为:" + (number1>=number2));
        System.out.println("number1<=number2的返回值为:" + (number1<=number2));
        System.out.println("number1!=number2的返回值为:" + (number1!=number2));    
        /*下面是书上P50表3.6第二条*/ 
        int number3 = 156;
        int number4 = 456;
        System.out.println("number3<number4的返回值为:" + (number3<number4));
	}

}
